package IfPrac;

public class exam01 {

	public static void main(String[] args) {
		System.out.print("\n");
		System.out.println();
		
		System.out.println(7);
		System.out.println(3.15);
		System.out.println("3 + 5 = "+(3+5));			//연산을 처리할 부분은 괄호로 묶어야 처리됨
		System.out.println(3.15+"는 실수입니다.");		//상수, 매개변수는 그냥 사용가능
														//여러 문자열과 매개변수를 사용할 경우 연결기호(+)를 사용
	}

}
